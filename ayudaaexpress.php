<h3>¿ Qué es el CVV?</h3>
<p>CVV2 es una medida de seguridad que requerimos para toda transacción. Ya que el número CVV2 se encuentra en su tarjeta de crédito pero no se almacena en ningún lado, la única forma de saber cuál es el número CVV2 correcto es cuando se tiene posesión física de la tarjeta en sí. Todas las tarjetas de crédito VISA, MasterCard, Discover y American Express que se fabricaron en los Estados Unidos en los últimos 5 años más o menos, cuentan con un número CVV. Cómo encontrar su número CVV:</p>
<p>En las tarjetas American Express, el número CVV2 es el número de dígitos que aparece arriba del final del número de su tarjeta. (Vea a continuación).</p>

